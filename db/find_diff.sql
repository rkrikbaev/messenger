SELECT
	ALL_DATES.DATETIME
FROM
	(
		SELECT
			DATETIME
		FROM
			LOGGER."407001"
		INTERSECT
		SELECT
			DATETIME
		FROM
			LOGGER."407002"
		INTERSECT
		SELECT
			DATETIME
		FROM
			LOGGER."407003"
		INTERSECT
		SELECT
			DATETIME
		FROM
			LOGGER."407004"
		INTERSECT
		SELECT
			DATETIME
		FROM
			LOGGER."407005"
		INTERSECT
		SELECT
			DATETIME
		FROM
			LOGGER."407006"
		INTERSECT
		SELECT
			DATETIME
		FROM
			LOGGER."407007"
		INTERSECT
		SELECT
			DATETIME
		FROM
			LOGGER."407008"
		INTERSECT
		SELECT
			DATETIME
		FROM
			LOGGER."407009"
	) AS ALL_DATES
	LEFT JOIN MESSENGER."DOCUMENTS" ON ALL_DATES.DATETIME = MESSENGER."DOCUMENTS".EVENT_DT
WHERE
	MESSENGER."DOCUMENTS".EVENT_DT IS NULL